    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLEASE FILL ALL EMPTY FIELDS", "Error", JOptionPane.WARNING_MESSAGE);
        } else {

            if (jTextField2.getText().length() > 4) {
                try {

                    Registry registry = LocateRegistry.getRegistry("127.0.0.1", 5000);
                    UserService intrf = (UserService) registry.lookup("user");

                    User use = new User();
                    use.setUsername(jTextField1.getText());
                    use.setPassword(jTextField2.getText());

                    String feedback = intrf.newuser(use);
                    JOptionPane.showMessageDialog(this, feedback, "successfuly", JOptionPane.INFORMATION_MESSAGE);
                    redo();

                } catch (Exception e) {
                    e.printStackTrace();
                }

            } else {
                JOptionPane.showMessageDialog(this, "PLEASE ENTER A PASSWORD ABOVE 5 DIGITS", "Error", JOptionPane.WARNING_MESSAGE);

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

