    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String password = jTextField2.getText();
        try {

            Registry registry = LocateRegistry.getRegistry("127.0.0.1", 5000);
            UserService service = (UserService) registry.lookup("user");
            List<User> userlist = service.Login(username, password);
            if (!userlist.isEmpty()) {
                int choice = JOptionPane.showOptionDialog(this, "Successfully Logged in", "Login", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new String[]{"Login as User", "Login as Supplier"}, null);

                if (choice == JOptionPane.YES_OPTION) {
                    dispose();
                    EndUser open = new EndUser();
                    open.setVisible(true);
                } else if (choice == JOptionPane.NO_OPTION) {
                    dispose();
                    Supplier open = new Supplier();
                    open.setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Invalid Username or Password");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

